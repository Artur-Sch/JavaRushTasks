package com.javarush.task.task04.task0433;


/* 
Гадание на долларовый счет
*/

import java.io.*;

public class Solution {
    public static void main(String[] args) throws Exception {
        int n = 10;
        String s = "SSSSSSSSSS";
        while (n > 0) {
            System.out.println(s);
            n--;
        }

    }
}
